  export default {
    data() {
      return {
        activeItem: '',
        openSubMenus: ['/sq-admin/course', '#pengaturan'], // Add all submenu paths you want to expand by default
      };
    },
    methods: {
      setActive(item) {
        if (this.activeItem !== item) {
          this.activeItem = item;
          this.openSubMenus = [item];
        }
      },
      isOpen(menu) {
        return this.openSubMenus.includes(menu);
      },
      toggleSubMenu(menu) {
        if (this.activeItem !== menu) {
          if (this.isOpen(menu)) {
            this.openSubMenus = this.openSubMenus.filter((m) => m !== menu);
          } else {
            this.openSubMenus.push(menu);
          }
        }
      },
      menuItemClass(item) {
        return {
          'block rounded-lg px-4 py-2 text-sm font-medium': true,
          'text-gray-500 hover:bg-emerald-500 hover:text-gray-700': this.activeItem !== item,
          'bg-emerald-100 text-gray-700': this.activeItem === item,
        };
      },
    },
  };
